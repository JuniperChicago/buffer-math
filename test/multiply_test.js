const expect = require('chai').expect
const Buffer = require('buffer').Buffer
const multiply = require('../lib/multiply')

const ZERO = Buffer.from([0])
const ONE = Buffer.from([1])
const BIG_NUM_1 = Buffer.from([0x8F, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x15])
const BIG_NUM_2 = Buffer.from([0x7A, 0xCB, 0x5C, 0x38, 0xAC, 0x6D, 0xAE, 0xAA])

it('multiply', function () {
  expect(multiply(ZERO, ZERO)).to.deep.equal(ZERO)
  expect(multiply(ONE, ZERO)).to.deep.equal(ZERO)
  expect(multiply(BIG_NUM_1, BIG_NUM_2)).to.deep.equal(Buffer.from([0x26, 0x2F, 0x0A, 0x1F, 0xFB, 0x96, 0x01, 0xA9, 0x30, 0x7C, 0xF9, 0xD7, 0xCC, 0x6C, 0xFD, 0xAA, 0x0E]))
  expect(multiply(BIG_NUM_2, BIG_NUM_1)).to.deep.equal(Buffer.from([0x26, 0x2F, 0x0A, 0x1F, 0xFB, 0x96, 0x01, 0xA9, 0x30, 0x7C, 0xF9, 0xD7, 0xCC, 0x6C, 0xFD, 0xAA, 0x0E]))
})
