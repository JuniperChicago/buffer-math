const expect = require('chai').expect
const Buffer = require('buffer').Buffer
const not = require('../lib/not')

const ZERO = Buffer.from([0])
const ONE = Buffer.from([1])
const BIG_NUM_1 = Buffer.from([0x7F, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x15])
const BIG_NUM_2 = Buffer.from([0x7A, 0xCB, 0x5C, 0x38, 0xAC, 0x6D, 0xAE, 0xAA])

it('not', function () {
  expect(not(ZERO)).to.deep.equal(Buffer.from([0xFF]))
  expect(not(ONE)).to.deep.equal(Buffer.from([0xFE]))
  expect(not(BIG_NUM_1)).to.deep.equal(Buffer.from([0x80, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xEA]))
  expect(not(BIG_NUM_2)).to.deep.equal(Buffer.from([0x85, 0x34, 0xA3, 0xC7, 0x53, 0x92, 0x51, 0x55]))
})
